package Assignment2;

public class CompareTwoValues {

	public static void main(String[] args) {

		int x = 45;
		int y = 15;
		
		if ( x > y )
		System.out.print("x is bigger");
		
		else if ( y < x )
		System.out.print("y is bigger");
		
		else if ( x == y)
		System.out.print("Both are the same");
		
		else
		System.out.print("error");		
	}
}
